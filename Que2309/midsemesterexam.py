# 2. Find the intersection of three sets, for example A = {1,2,3,4}, B = {4,5,6,7}, C = {5,9,8,12}, in such a way that the element must be present in exactly two sets, not in one and not in all three.


# 3. Find the intersection of two lists such that the order of the first list is maintained.


# 4. You are given a list of strings, each string containing a mixture of brackets like ()[]{}. Write a program to check whether for each string the opening and closing brackets are correctly ordered (balanced) or not.

# 5. You are given a list of n integers and a step size k. Starting from the first element, move forward with step size k such that all elements are visited at least once. Find the sum of the visited elements.